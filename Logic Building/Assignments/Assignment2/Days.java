class Days{

          public static void main(String args[]){
		  
		  int day = 1;
		  System.out.println("The choosen day is :"+ day);
		  
		  switch(day){
			  
			  case 1: 
			  System.out.println("Its a Weekday Monday");
			  break;
			  
			  case 2: 
			  System.out.println("Its a Weekday Tuesday");
			  break;
			  
			  case 3: 
			  System.out.println("Its a Weekday Wedesday");
			  break;
			  
			  case 4: 
			  System.out.println("Its a Weekday Thursday");
			  break;
			  
			  case 5: 
			  System.out.println("Its a Weekday Friday");
			  break;
			  
			  case 6: 
			  System.out.println("Its a Weekend  Saturday");
			  break;
			  
			  case 7: 
			  System.out.println("Its a Weekend Sunday");
			  break;
			  
			  default : 
			  System.out.println("Please choose appropriate number");
			  break;
			  
			  
		  }
		  }

}